putaran = 5
jarak = 14
pi = 3.14

keliling = jarak / putaran

r = keliling / (2 * pi)

print("Diketahui :")
print("Pak Dengklek mengelilingi taman =", putaran, "Putaran")
print("Jarak tempuh Pak Dengklek =", jarak, "Kilometer")
print()
print("Jawaban :")
print("Jari-jari taman yang dikelilingi Pak Dengklek adalah", round(r, 2), "kilometer")